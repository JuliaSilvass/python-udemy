'''
Use a função range() em um "for loop" para imprimir os números de 1 a 10 na tela
'''
for num in range (1,11,1):   
    print(num)

'''
(1,11,1)

primeiro 1 é onde inicia, geralmente em 0 se não for definido,
o 11 é o stop, onde o for vai para, ele não é incluido no print,
O ultimo 1 é step é de quanto em quanto será contado,
geralmente é de 1 em 1 se não definido.
'''